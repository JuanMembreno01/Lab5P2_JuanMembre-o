    private void jtreedecanoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtreedecanoMouseClicked
        if (evt.isMetaDown()) {
            //seleccionar un nodo con click derecho
            int row = jtreedecano.getClosestRowForLocation(
                    evt.getX(), evt.getY());
            jtreedecano.setSelectionRow(row);
            Object v1 = jtreedecano.getSelectionPath().getLastPathComponent();
            nodo_seleccionado = (DefaultMutableTreeNode) v1;

            if (nodo_seleccionado.getUserObject() instanceof docente) {
                docente_seleccionada
                        = (docente) nodo_seleccionado.
                                getUserObject();
                crud.show(evt.getComponent(),
                        evt.getX(), evt.getY());
            }
        }
    }//GEN-LAST:event_jtreedecanoMouseClicked

