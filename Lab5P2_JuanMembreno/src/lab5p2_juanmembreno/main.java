    private void jMenu5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenu5ActionPerformed

        DefaultTreeModel m = (DefaultTreeModel) jtreedecano.getModel();
        DefaultMutableTreeNode raiz
                = (DefaultMutableTreeNode) m.getRoot();
        DefaultMutableTreeNode nodo_docente;
        for (int i = 0; i < listadocente.size(); i++) {
            nodo_docente
                    = new DefaultMutableTreeNode(
                            new docente(listadocente.get(i).getNombre(), listadocente.get(i).getApellido(), listadocente.get(i).getTitulo(), listadocente.get(i).getTitulomaestria(), listadocente.get(i).getCantidaddealumnosmax(), listadocente.get(i).getNombreclase(), listadocente.get(i).getDescripcion(), listadocente.get(i).getUsuario(), listadocente.get(i).getContrasena()
                            )
                    );

            DefaultMutableTreeNode alumno;
            alumno = new DefaultMutableTreeNode(new alumnos());

            nodo_docente.add(alumno);
            raiz.add(nodo_docente);
            m.reload();
            abreli();
        }
    }//GEN-LAST:event_jMenu5ActionPerformed

